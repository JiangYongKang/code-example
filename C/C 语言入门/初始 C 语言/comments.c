#include <stdio.h>
int main()
{
    // 这是一首思念家乡的诗
    printf("床前明月光，");
    printf("疑是地上霜。");
    printf("举头望明月，");
    printf("低头思故乡。");
    /*短短四句诗，写得清新朴素，明白如话。它的内容是单纯的，但同时却又是丰富的。
    它是容易理解的，却又是体味不尽的。诗人所没有说的比他已经说出来的要多得多。
    它的构思是细致而深曲的，但却又是脱口吟成、浑然无迹的。从这里，我们不难领
    会到李白绝句的“自然”、“无意于工而无不工”的妙境。*/
    return 0;
}
